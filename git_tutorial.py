def add_number(a, b, c):
    """
    Adds three numbers together.
    
    Parameters:
    a (int or float): The first number.
    b (int or float): The second number.
    c (int or float): The third number.
    
    Returns:
    int or float: The sum of a, b, and c.

    replace addition with sum
    """
    return sum([a, b, c])
